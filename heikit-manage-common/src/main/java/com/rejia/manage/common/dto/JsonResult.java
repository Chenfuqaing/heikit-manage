public class JsonResult {
	private boolean result;
	private String msg;
	
	public boolean getResult() {
		return result;
	}
	public void setResult(boolean result) {
		this.result = result;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public JsonResult() {
	}
	public JsonResult(boolean result, String msg) {
		this.result = result;
		this.msg = msg;
	}
}
